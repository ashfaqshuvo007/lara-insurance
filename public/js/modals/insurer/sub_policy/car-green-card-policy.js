$('#add-green-card-car-policy').submit(function(event){
    event.preventDefault();
    var inputIds = {};
    var $inputs = $('#add-green-card-car-policy :input');
    $inputs.each(function (index)
    {
        inputIds[$(this).attr('name')] = $(this).attr('id');
    });
    let id = $(this).attr('id');
    let idObj = {
        'id':id,
        'inputIds':inputIds,
        'error_msg':'error_travel_sub1',
        'reload_page':'tpl',
    }
    globalSubmit(idObj);
});
